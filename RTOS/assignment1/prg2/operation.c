#include "header.h"
#include<stdio.h>

//Function to compute sum
int sum(int num, int arr_sum){
        return num + arr_sum;
}

//Function to compute average
float average(int arr_sum, float count){
        return (arr_sum/count);
}

//Function to compute product
int product(int num, int arr_prod){
        return num * arr_prod;
}

